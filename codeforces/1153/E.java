	static class EServalAndSnake {
		int n;

		public EServalAndSnake() {
		}

		public Pair<Integer, Integer> search(Function<Integer, Boolean> f) {
			int a = -1, b = -1;
			for(int i = 1; i<=n; i++) {
				if(f.apply(i)) {
					if(a==-1) {
						a = i;
					}else {
						b = i;
					}
				}
			}
			return new Pair<>(a, b);
		}

		public int bsearch(BiFunction<Integer, Integer, Boolean> f) {
			int l = 1, r = n;
			while(l<r) {
				int mid = l+r >> 1;
				if(f.apply(l, mid)) {
					r = mid;
				}else {
					l = mid+1;
				}
			}
			return l;
		}

		public void solve(int kase, InputReader in, Output pw) {
			n = in.nextInt();
			var x = search(i -> {
				pw.println("?", i, 1, i, n);
				pw.flush();
				return (in.nextInt()&1)==1;
			});
			if(x.a!=-1) {
				pw.println("!", x.a,
						bsearch((a, b) -> {
							pw.println("?", x.a, a, x.a, b);
							pw.flush();
							return (in.nextInt()&1)==1;
						}),
						x.b,
						bsearch((a, b) -> {
							pw.println("?", x.b, a, x.b, b);
							pw.flush();
							return (in.nextInt()&1)==1;
						}));
				return;
			}
			var y = search(i -> {
				pw.println("?", 1, i, n, i);
				pw.flush();
				return (in.nextInt()&1)==1;
			});
			int xx = bsearch((a, b) -> {
				pw.println("?", a, y.a, b, y.a);
				pw.flush();
				return (in.nextInt()&1)==1;
			});
			pw.println("!", xx, y.a, xx, y.b);
			pw.flush();
		}

	}

	static class Pair<T1, T2> implements Comparable<Pair<T1, T2>> {
		public T1 a;
		public T2 b;

		public Pair(Pair<T1, T2> p) {
			this(p.a, p.b);
		}

		public Pair(T1 a, T2 b) {
			this.a = a;
			this.b = b;
		}

		public String toString() {
			return a+" "+b;
		}

		public int hashCode() {
			return Objects.hash(a, b);
		}

		public boolean equals(Object o) {
			if(o instanceof Pair) {
				Pair p = (Pair) o;
				return a.equals(p.a)&&b.equals(p.b);
			}
			return false;
		}

		public int compareTo(Pair<T1, T2> p) {
			int cmp = ((Comparable<T1>) a).compareTo(p.a);
			if(cmp==0) {
				return ((Comparable<T2>) b).compareTo(p.b);
			}
			return cmp;
		}

	}

	static class Output implements Closeable, Flushable {
		public StringBuilder sb;
		public OutputStream os;
		public int BUFFER_SIZE;
		public String lineSeparator;

		public Output(OutputStream os) {
			this(os, 1<<16);
		}

		public Output(OutputStream os, int bs) {
			BUFFER_SIZE = bs;
			sb = new StringBuilder(BUFFER_SIZE);
			this.os = new BufferedOutputStream(os, 1<<17);
			lineSeparator = System.lineSeparator();
		}

		public void print(String s) {
			sb.append(s);
			if(sb.length()>BUFFER_SIZE >> 1) {
				flushToBuffer();
			}
		}

		public void println(Object... o) {
			for(int i = 0; i<o.length; i++) {
				if(i!=0) {
					print(" ");
				}
				print(String.valueOf(o[i]));
			}
			println();
			if(sb.length()>BUFFER_SIZE >> 1) {
				flushToBuffer();
			}
		}

		public void println() {
			sb.append(lineSeparator);
		}

		private void flushToBuffer() {
			try {
				os.write(sb.toString().getBytes());
			}catch(IOException e) {
				e.printStackTrace();
			}
			sb = new StringBuilder(BUFFER_SIZE);
		}

		public void flush() {
			try {
				flushToBuffer();
				os.flush();
			}catch(IOException e) {
				e.printStackTrace();
			}
		}

		public void close() {
			flush();
			try {
				os.close();
			}catch(IOException e) {
				e.printStackTrace();
			}
		}

	}

	static class FastReader implements InputReader {
		final private int BUFFER_SIZE = 1<<16;
		private DataInputStream din;
		private byte[] buffer;
		private int bufferPointer;
		private int bytesRead;

		public FastReader(InputStream is) {
			din = new DataInputStream(is);
			buffer = new byte[BUFFER_SIZE];
			bufferPointer = bytesRead = 0;
		}

		public int nextInt() {
			int ret = 0;
			byte c = skipToDigit();
			boolean neg = (c=='-');
			if(neg) {
				c = read();
			}
			do {
				ret = ret*10+c-'0';
			} while((c = read())>='0'&&c<='9');
			if(neg) {
				return -ret;
			}
			return ret;
		}

		private boolean isDigit(byte b) {
			return b>='0'&&b<='9';
		}

		private byte skipToDigit() {
			byte ret;
			while(!isDigit(ret = read())&&ret!='-') ;
			return ret;
		}

		private void fillBuffer() {
			try {
				bytesRead = din.read(buffer, bufferPointer = 0, BUFFER_SIZE);
			}catch(IOException e) {
				e.printStackTrace();
				throw new InputMismatchException();
			}
			if(bytesRead==-1) {
				buffer[0] = -1;
			}
		}

		private byte read() {
			if(bytesRead==-1) {
				throw new InputMismatchException();
			}else if(bufferPointer==bytesRead) {
				fillBuffer();
			}
			return buffer[bufferPointer++];
		}

	}

	static interface InputReader {
		int nextInt();

	}
