	static class DBearAndFloodlight {
		double r;

		public DBearAndFloodlight() {
		}

		private double angle(double x, double y, double xx) {
			return Math.atan2(xx-x, y);
		}

		private double dist(double x, double y, double deg, double xx) {
			double ang1 = angle(x, y, xx)+deg, ang2 = angle(x, y, r);
			if(ang1>ang2+1e-6) {
				return r;
			}
			return Math.max(x+Math.tan(ang1)*y, xx);
		}

		public void solve(int kase, InputReader in, Output pw) {
			int n = in.nextInt();
			double l = in.nextInt();
			r = in.nextInt()-l;
			double[][] arr = new double[n][3];
			for(int i = 0; i<n; i++) {
				arr[i] = new double[] {in.nextInt()-l, in.nextInt(), Math.toRadians(in.nextInt())};
			}
			double x = dist(100000, 1, Math.PI/2, 0);
			Utilities.Debug.dbg(x);
			Utilities.Debug.dbg(dist(100000, 2, Math.PI/2, x));
			double[] dp = new double[1<<n];
			for(int i = 1; i<1<<n; i++) {
				for(int j = 0; j<n; j++) {
					if((i&1<<j)>0) {
						dp[i] = Math.max(dp[i], dist(arr[j][0], arr[j][1], arr[j][2], dp[i^1<<j]));
					}
				}
			}
			Utilities.Debug.dbg(arr, r);
			Utilities.Debug.dbg(dp);
			pw.printf("%.6f\n", Math.min(r, dp[(1<<n)-1]));
		}

	}

	static class Output implements Closeable, Flushable {
		public StringBuilder sb;
		public OutputStream os;
		public int BUFFER_SIZE;
		public String lineSeparator;

		public Output(OutputStream os) {
			this(os, 1<<16);
		}

		public Output(OutputStream os, int bs) {
			BUFFER_SIZE = bs;
			sb = new StringBuilder(BUFFER_SIZE);
			this.os = new BufferedOutputStream(os, 1<<17);
			lineSeparator = System.lineSeparator();
		}

		public void print(String s) {
			sb.append(s);
			if(sb.length()>BUFFER_SIZE >> 1) {
				flushToBuffer();
			}
		}

		public void printf(String s, Object... o) {
			print(String.format(s, o));
		}

		private void flushToBuffer() {
			try {
				os.write(sb.toString().getBytes());
			}catch(IOException e) {
				e.printStackTrace();
			}
			sb = new StringBuilder(BUFFER_SIZE);
		}

		public void flush() {
			try {
				flushToBuffer();
				os.flush();
			}catch(IOException e) {
				e.printStackTrace();
			}
		}

		public void close() {
			flush();
			try {
				os.close();
			}catch(IOException e) {
				e.printStackTrace();
			}
		}

	}

