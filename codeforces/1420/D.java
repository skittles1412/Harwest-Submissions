	static class DRescueNibel {
		private final int mod = 998244353;

		public DRescueNibel() {
		}

		public void solve(int kase, InputReader in, Output pw) {
			Utilities.Debug.dbg((300000L*299999/2)%mod);
			int n = in.nextInt(), k = in.nextInt();
			long[] choose = new long[n+1];
			choose[k] = 1;
			for(int i = k+1; i<=n; i++) {
				choose[i] = ((choose[i-1]*i)%mod*Utilities.math.pow(i-k, mod-2, mod))%mod;
			}
			Pair<Integer, Integer>[] arr = new Pair[n<<1];
			for(int i = 0; i<n; i++) {
				arr[i<<1] = new Pair<>(in.nextInt(), 1);
				arr[i<<1|1] = new Pair<>(in.nextInt(), -1);
			}
			Arrays.sort(arr, (a, b) -> {
				if(a.a.equals(b.a)) {
					return b.b-a.b;
				}
				return a.a-b.a;
			});
			long ans = 0;
			int count = 0, start = 0, prev = -1;
			for(int i = 0; i<arr.length; i++) {
				var v = arr[i];
				if(v.b==-1) {
					count--;
				}else {
					if(v.a!=prev) {
						prev = v.a;
						start = 0;
					}
					start++;
					count++;
					if(i==arr.length-1||!v.a.equals(arr[i+1].a)||!v.b.equals(arr[i+1].b)) {
						Utilities.Debug.dbg(prev, start, count);
						ans = (ans+choose[count]-choose[count-start]+mod)%mod;
					}
				}
			}
			pw.println(ans);
		}

	}

	static class Pair<T1, T2> implements Comparable<Pair<T1, T2>> {
		public T1 a;
		public T2 b;

		public Pair(Pair<T1, T2> p) {
			this(p.a, p.b);
		}

		public Pair(T1 a, T2 b) {
			this.a = a;
			this.b = b;
		}

		public String toString() {
			return a+" "+b;
		}

		public int hashCode() {
			return Objects.hash(a, b);
		}

		public boolean equals(Object o) {
			if(o instanceof Pair) {
				Pair p = (Pair) o;
				return a.equals(p.a)&&b.equals(p.b);
			}
			return false;
		}

		public int compareTo(Pair<T1, T2> p) {
			int cmp = ((Comparable<T1>) a).compareTo(p.a);
			if(cmp==0) {
				return ((Comparable<T2>) b).compareTo(p.b);
			}
			return cmp;
		}

	}

