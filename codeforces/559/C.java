	static class CGeraldAndGiantChess {
		private final int mod = (int) (1e9+7);
		long[] fact;
		long[] ifact;

		public CGeraldAndGiantChess() {
			int maxn = (int) 2e5+5;
			fact = new long[maxn];
			for(int i = 0; i<maxn; i++) {
				fact[i] = i;
			}
			fact[0] = 1;
			Arrays.parallelPrefix(fact, (a, b) -> a*b%mod);
			ifact = Utilities.math.modInverse(maxn, mod);
			ifact[0] = 1;
			Arrays.parallelPrefix(ifact, (a, b) -> a*b%mod);
		}

		public long ways(int x, int y) {
			return (((fact[x+y]*ifact[x])%mod)*ifact[y])%mod;
		}

		public void solve(int kase, InputReader in, Output pw) {
			int a = in.nextInt(), b = in.nextInt(), n = in.nextInt();
			int[][] arr = new int[n+1][2];
			for(int i = 0; i<n; i++) {
				arr[i] = in.nextInt(2);
			}
			arr[n] = new int[] {a, b};
			Arrays.sort(arr, Arrays::compare);
			long[] dp = new long[n+1];
			for(int i = 0; i<=n; i++) {
				dp[i] = ways(arr[i][0]-1, arr[i][1]-1);
				for(int j = 0; j<i; j++) {
					if(arr[j][1]<=arr[i][1]) {
						dp[i] += mod-(dp[j]*ways(arr[i][0]-arr[j][0], arr[i][1]-arr[j][1]))%mod;
					}
				}
				dp[i] %= mod;
			}
//			Utilities.Debug.dbg(dp);
			pw.println(dp[n]);
		}

	}

