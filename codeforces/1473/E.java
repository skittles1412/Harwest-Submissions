	static class EMinimumPath {
		public EMinimumPath() {
		}

		public void solve(int kase, InputReader in, Output pw) {
			int n = in.nextInt(), m = in.nextInt();
			var graph = in.nextUndirectedWeightedGraph(n, m);
			long[][] dist = new long[n][4];
			for(int i = 0; i<n; i++) {
				dist[i][0] = dist[i][1] = dist[i][2] = dist[i][3] = -1;
			}
			PriorityQueue<EMinimumPath.State> pq = new PriorityQueue<>((int) 4e7, Comparator.comparingLong(o -> o.dist));
			pq.add(new EMinimumPath.State(0, 0, 0));
			while(!pq.isEmpty()) {
				var cur = pq.poll();
				if(dist[cur.u][cur.x]!=-1) {
					continue;
				}
				dist[cur.u][cur.x] = cur.dist;
				for(var v: graph[cur.u]) {
					for(int i = 0; i<4; i++) {
						if(dist[v.a][cur.x|i]==-1&&(i&cur.x)==0) {
							if(i==0||i==3) {
								pq.add(new EMinimumPath.State(v.a, cur.x|i, cur.dist+v.b));
							}else if(i==1) {
								pq.add(new EMinimumPath.State(v.a, cur.x|i, cur.dist+2L*v.b));
							}else {
								pq.add(new EMinimumPath.State(v.a, cur.x|i, cur.dist));
							}
						}
					}
				}
			}
//			Utilities.Debug.dbg(dist);
			for(int i = 1; i<n; i++) {
				pw.print(dist[i][3]+" ");
			}
		}

		static class State {
			int u;
			int x;
			long dist;

			public State(int u, int x, long dist) {
				this.u = u;
				this.x = x;
				this.dist = dist;
			}

		}

	}

	static class Pair<T1, T2> implements Comparable<Pair<T1, T2>> {
		public T1 a;
		public T2 b;

		public Pair(Pair<T1, T2> p) {
			this(p.a, p.b);
		}

		public Pair(T1 a, T2 b) {
			this.a = a;
			this.b = b;
		}

		public String toString() {
			return a+" "+b;
		}

		public int hashCode() {
			return Objects.hash(a, b);
		}

		public boolean equals(Object o) {
			if(o instanceof Pair) {
				Pair p = (Pair) o;
				return a.equals(p.a)&&b.equals(p.b);
			}
			return false;
		}

		public int compareTo(Pair<T1, T2> p) {
			int cmp = ((Comparable<T1>) a).compareTo(p.a);
			if(cmp==0) {
				return ((Comparable<T2>) b).compareTo(p.b);
			}
			return cmp;
		}

	}

	static interface InputReader {
		int nextInt();

		default ArrayList<Pair<Integer, Integer>>[] nextUndirectedWeightedGraph(int n, int m) {
			ArrayList<Pair<Integer, Integer>>[] ret = new ArrayList[n];
			for(int i = 0; i<n; i++) {
				ret[i] = new ArrayList<>();
			}
			for(int i = 0; i<m; i++) {
				int u = nextInt()-1, v = nextInt()-1, w = nextInt();
				ret[u].add(new Pair<>(v, w));
				ret[v].add(new Pair<>(u, w));
			}
			return ret;
		}

	}

	static class Output implements Closeable, Flushable {
		public StringBuilder sb;
		public OutputStream os;
		public int BUFFER_SIZE;
		public String lineSeparator;

		public Output(OutputStream os) {
			this(os, 1<<16);
		}

		public Output(OutputStream os, int bs) {
			BUFFER_SIZE = bs;
			sb = new StringBuilder(BUFFER_SIZE);
			this.os = new BufferedOutputStream(os, 1<<17);
			lineSeparator = System.lineSeparator();
		}

		public void print(String s) {
			sb.append(s);
			if(sb.length()>BUFFER_SIZE >> 1) {
				flushToBuffer();
			}
		}

		private void flushToBuffer() {
			try {
				os.write(sb.toString().getBytes());
			}catch(IOException e) {
				e.printStackTrace();
			}
			sb = new StringBuilder(BUFFER_SIZE);
		}

		public void flush() {
			try {
				flushToBuffer();
				os.flush();
			}catch(IOException e) {
				e.printStackTrace();
			}
		}

		public void close() {
			flush();
			try {
				os.close();
			}catch(IOException e) {
				e.printStackTrace();
			}
		}

	}

	static class FastReader implements InputReader {
		final private int BUFFER_SIZE = 1<<16;
		private DataInputStream din;
		private byte[] buffer;
		private int bufferPointer;
		private int bytesRead;

		public FastReader(InputStream is) {
			din = new DataInputStream(is);
			buffer = new byte[BUFFER_SIZE];
			bufferPointer = bytesRead = 0;
		}

		public int nextInt() {
			int ret = 0;
			byte c = skipToDigit();
			boolean neg = (c=='-');
			if(neg) {
				c = read();
			}
			do {
				ret = ret*10+c-'0';
			} while((c = read())>='0'&&c<='9');
			if(neg) {
				return -ret;
			}
			return ret;
		}

		private boolean isDigit(byte b) {
			return b>='0'&&b<='9';
		}

		private byte skipToDigit() {
			byte ret;
			while(!isDigit(ret = read())&&ret!='-') ;
			return ret;
		}

		private void fillBuffer() {
			try {
				bytesRead = din.read(buffer, bufferPointer = 0, BUFFER_SIZE);
			}catch(IOException e) {
				e.printStackTrace();
				throw new InputMismatchException();
			}
			if(bytesRead==-1) {
				buffer[0] = -1;
			}
		}

		private byte read() {
			if(bytesRead==-1) {
				throw new InputMismatchException();
			}else if(bufferPointer==bytesRead) {
				fillBuffer();
			}
			return buffer[bufferPointer++];
		}

	}

