	static class EMakeItIncreasing {
		private final int iinf = 1_000_000_000;

		public EMakeItIncreasing() {
		}

		public int solve(int[] arr, int l, int r) {
			int n = arr.length, ans = -1;
			int[] val = new int[n], ind = new int[n];
			Arrays.fill(val, iinf+1);
			for(int i = 0; i<n; i++) {
				if(arr[i]<r-n+i+1&&arr[i]>l+i) {
					int a = -1, b = ans;
					while(a<b) {
						int mid = (a+b+1)/2;
						if(arr[i]<=val[mid]||i-ind[mid]>arr[i]-val[mid]) {
							b = mid-1;
						}else {
							a = mid;
						}
					}
					ans = Math.max(ans, a+1);
					if(arr[i]-i<val[a+1]-ind[a+1]) {
						val[a+1] = arr[i];
						ind[a+1] = i;
					}
				}
			}
			return n-ans-1;
		}

		public void solve(int kase, InputReader in, Output pw) {
			int n = in.nextInt(), k = in.nextInt();
			int[] arr = in.nextInt(n);
			boolean[] valid = new boolean[n];
			Arrays.fill(valid, true);
			for(int i = 0; i<k; i++) {
				valid[in.nextInt()-1] = false;
			}
			for(int i = 0; i<n; i++) {
				for(; i<n&&valid[i]; i++) ;
				if(i==n) {
					break;
				}
				int prev = -1;
				for(; i<n&&!valid[i]; i++) {
					if(arr[i]<=prev) {
						pw.println("-1");
						return;
					}
					prev = arr[i];
				}
			}
			int ans = 0;
			for(int i = 0; i<n; i++) {
				for(; i<n&&!valid[i]; i++) ;
				if(i==n) {
					break;
				}
				int start = i;
				for(; i<n&&valid[i]; i++) ;
				int l = start==0 ? -2*n : arr[start-1], r = i==n ? iinf+2*n : arr[i];
				if(r-l-1<i-start) {
					pw.println("-1");
					return;
				}
				ans += solve(Arrays.copyOfRange(arr, start, i), l, r);
//				Utilities.Debug.dbg(ans);
			}
			pw.println(ans);
		}

	}

	static class Output implements Closeable, Flushable {
		public StringBuilder sb;
		public OutputStream os;
		public int BUFFER_SIZE;
		public String lineSeparator;

		public Output(OutputStream os) {
			this(os, 1<<16);
		}

		public Output(OutputStream os, int bs) {
			BUFFER_SIZE = bs;
			sb = new StringBuilder(BUFFER_SIZE);
			this.os = new BufferedOutputStream(os, 1<<17);
			lineSeparator = System.lineSeparator();
		}

		public void println(int i) {
			println(String.valueOf(i));
		}

		public void println(String s) {
			sb.append(s);
			println();
		}

		public void println() {
			sb.append(lineSeparator);
		}

		private void flushToBuffer() {
			try {
				os.write(sb.toString().getBytes());
			}catch(IOException e) {
				e.printStackTrace();
			}
			sb = new StringBuilder(BUFFER_SIZE);
		}

		public void flush() {
			try {
				flushToBuffer();
				os.flush();
			}catch(IOException e) {
				e.printStackTrace();
			}
		}

		public void close() {
			flush();
			try {
				os.close();
			}catch(IOException e) {
				e.printStackTrace();
			}
		}

	}

